import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    cur_1 = l1
    cur_2 = l2
    total = cur_1.val + cur_2.val
    next_add_one = False
    if total >= 10:
        total -= 10
        next_add_one = True
    l3 = ListNode(total, None)
    cur_3 = l3
    while cur_1.next is not None or cur_2.next is not None:
        total = 0
        if next_add_one:
            total += 1
            next_add_one = False
        if cur_1.next is not None:
            cur_1 = cur_1.next
            total += cur_1.val
        if cur_2.next is not None:
            cur_2 = cur_2.next
            total += cur_2.val

        if total >= 10:
            total = total - 10
            next_add_one = True
        cur_3.next = ListNode(total, None)
        cur_3 = cur_3.next
    if next_add_one:
        cur_3.val = 1
    return l3

